export function calculateEstimate(product, materials, quantity) {
  const totalMaterialCost = product.materials.reduce((sum, req) => {
    const mat = materials.find(m => m.id === req.materialId);
    return sum + (mat ? mat.unitCost * req.qty * quantity : 0);
  }, 0);
  const totalLabourCost = product.labourCost * quantity;
  const overhead = (totalMaterialCost + totalLabourCost) * 0.15;
  const totalInternalCost = totalMaterialCost + totalLabourCost + overhead;
  const totalProposal = totalInternalCost * 1.35;
  
  return { 
    totalMaterialCost, 
    totalLabourCost, 
    overhead, 
    totalInternalCost, 
    totalProposal 
  };
}

export function getInventoryStatus(product, inventory, materials) {
  return product.materials.map(req => {
    const inv = inventory.find(i => i.materialId === req.materialId);
    const mat = materials.find(m => m.id === req.materialId);
    return {
      materialId: req.materialId,
      name: mat ? mat.name : "Unknown Material",
      status: inv ? inv.status : "out_of_stock",
      stock: inv ? inv.stock : 0
    };
  });
}
